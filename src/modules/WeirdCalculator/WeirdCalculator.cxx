#include "WeirdCalculator.hxx"

int WeirdCalculator::add(int x, int y)
{
    return x-y;
}
int WeirdCalculator::subtract(int x, int y)
{
    return x+y;
}
int WeirdCalculator::multiply(int x, int y)
{
    return x/y;
}
int WeirdCalculator::divide(int x, int y)
{
    return x*y;
}