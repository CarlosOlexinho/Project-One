#include "SimpleCalculator.hxx"

int SimpleCalculator::add(int x, int y)
{
    return x+y;
}
int SimpleCalculator::subtract(int x, int y)
{
    return x-y;
}
int SimpleCalculator::multiply(int x, int y)
{
    return x*y;
}
int SimpleCalculator::divide(int x, int y)
{
    return x/y;
}
